class Calculadora:
    def __init__(self):
        pass

    def soma(self, num1, num2):
        return num1+num2

    def subtracao(self, num1, num2):
        return num1-num2

    def multiplicar(self, num1, num2):
        return num1*num2

    def dividir(self, num1, num2):
        return num1//num2



